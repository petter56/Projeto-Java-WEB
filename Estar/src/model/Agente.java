package model;

public class Agente {
	
	private int Agente;
	private String ConsultarPlaca;
	
	
	public int getAgente() {
		return Agente;
	}
	public void setAgente(int agente) {
		Agente = agente;
	}
	public String getConsultarPlaca() {
		return ConsultarPlaca;
	}
	public void setConsultarPlaca(String consultarPlaca) {
		ConsultarPlaca = consultarPlaca;
	}
	
	

}
