package model;

public class EstacionarCarro {
	
	private int EstacionaID;
	private String Data;
	private String Horario;
	
	public int getEstacionaID() {
		return EstacionaID;
	}
	public void setEstacionaID(int estacionaID) {
		EstacionaID = estacionaID;
	}
	public String getData() {
		return Data;
	}
	public void setData(String data) {
		Data = data;
	}
	public String getHorario() {
		return Horario;
	}
	public void setHorario(String horario) {
		Horario = horario;
	}
	
	

}
